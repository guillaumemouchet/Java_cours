
package ch.arc.cours.z_annexe.material;

public class Calculatrice
	{

	/*------------------------------------------------------------------*\
	|*							Methodes Public							*|
	\*------------------------------------------------------------------*/

	/**
	 * en int
	 */
	public static int add(int x, int y)
		{
		return x + y;
		}

	/**
	 * en double
	 */
	public static double multiply(double i, double j)
		{
		return i * j;
		}

	/**
	 * en double
	 */
	public static double sqrt(double x)
		{
		return Math.sqrt(x);
		}

	/*------------------------------------------------------------------*\
	|*							Methodes Private						*|
	\*------------------------------------------------------------------*/

	}
